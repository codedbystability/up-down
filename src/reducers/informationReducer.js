const informationReducer = (state = informationStates, action) => {
    switch (action.type) {

        case INFORMATION_CONSTANTS.SET_INSTRUMENTS:
            return {
                ...state,
                instruments: action.data
            }

        case INFORMATION_CONSTANTS.SET_CALCULATOR_SYMBOL:
            console.log(INFORMATION_CONSTANTS.SET_CALCULATOR_SYMBOL, action.data)
            return {
                ...state,
                roiParity: action?.data,
                showSymbolList: false
            }

        case INFORMATION_CONSTANTS.SET_LAST_BAR:
            console.log(INFORMATION_CONSTANTS.SET_LAST_BAR, action.data)
            return {
                ...state,
                lastBar: action?.data,
            }
        case HANDLE_SOUNDS_SETTINGS:
            console.log(HANDLE_SOUNDS_SETTINGS, action.data)
            return {
                ...state,
                soundSettings: {
                    ...state.soundSettings,
                    [action.data.key]: action.data.value,
                },
            };

        case INFORMATION_CONSTANTS.SET_ACTIVE_CHART:
            return {
                ...state,
                activeChart: action.data
            }

        case 'SET_LAST_AMOUNT':
            return {
                ...state,
                lastAmount: action.data
            }
        case INFORMATION_CONSTANTS.SHOW_MY_BETS_ON_CHART:
            return {
                ...state,
                showMyBetsOnChart: action.data
            }


        case INFORMATION_CONSTANTS.SET_CHART_POSITION:
            console.log('SET_CHART_POSITION ', action.data)
            return {
                ...state,
                chartPosition: action.data
            }

        case INFORMATION_CONSTANTS.SET_BETS_ABLE:
            return {
                ...state,
                betsAble: action.data
            }

        case INFORMATION_CONSTANTS.SET_BET_WAY:
            return {
                ...state,
                betWay: action.data
            }

        case INFORMATION_CONSTANTS.SET_MODAL_TYPE:
            return {
                ...state,
                modalType: action.data,
                roiParity: action?.data === 'calculator' ? state?.activeParity : state.roiParity
            }

        case INFORMATION_CONSTANTS.SHOW_SYMBOL_LIST:
            return {
                ...state,
                showSymbolList: action.data
            }
        case INFORMATION_CONSTANTS.SET_MY_BETS:
            return {
                ...state,
                myBets: action.data,
                myBetsCount: action.data?.length || 0,
            }

        case INFORMATION_CONSTANTS.UPDATE_MY_BET:
            console.log('INFORMATION_CONSTANTS.UPDATE_MY_BET = ', action.data)
            return {
                ...state,
                myBets: state.myBets?.map(o => o.unique_id === action?.data?.unique_id ? {
                    ...o,
                    tp: action.data.tp,
                    sl: action.data.sl,
                } : o),
                myBetsCount: state.myBets?.length || 0,

            }
        case INFORMATION_CONSTANTS.REVISE_MY_BET:
            console.log('INFORMATION_CONSTANTS.REVISE_MY_BET => ', action?.data)
            // console.log('public = ',state.publicBets)
            return {
                ...state,
                myBets: state.myBets?.map(o => o.unique_id === action?.data?.unique_id ? action?.data : o),
                publicBets: state.publicBets?.map(o => o.unique_id === action?.data?.unique_id ? action?.data : o)
            }
        case INFORMATION_CONSTANTS.SET_POSITION_DETAIL:
            return {
                ...state,
                positionDetail: action.data,
                modalType: action.data ? 'detail' : ''
            }

        case 'SET_SHOW_PARITY_LIST':
            return {
                ...state,
                showParityList: action.data
            }
        case 'SET_TRANSACTIONS_SHEET':
            return {
                ...state,
                showTransactionsSheet: action.data
            }

        case INFORMATION_CONSTANTS.SET_NEW_BET:
            return {
                ...state,
                myBets: [action.data, ...state.myBets],
                myBetsCount: state.myBets?.length + 1 || 0,

            }

        case INFORMATION_CONSTANTS.SET_WHOLE_PUBLIC_BETS:
            return {
                ...state,
                publicBets: action.data
            }
        case INFORMATION_CONSTANTS.SET_WHOLE_ACTIVE_BETS:
            console.log(INFORMATION_CONSTANTS.SET_WHOLE_ACTIVE_BETS, action.data)
            return {
                ...state,
                myBets: action.data,
                myBetsCount: action?.data?.length || 0,

            }


        case INFORMATION_CONSTANTS.SET_PUBLIC_BETS:
            // console.log(' [action.data, ...state.publicBets] = ', [action.data, ...state.publicBets])
            const news = [action.data, ...state.publicBets].slice(0, 50);
            return {
                ...state,
                // publicBets: news?.length >= 20 ? news.slice(-20) : news.reverse()
                publicBets: news
            }

        case INFORMATION_CONSTANTS.RESULT_PUBLIC_BETS:
            console.log('INFORMATION_CONSTANTS.RESULT_PUBLIC_BETS = ', INFORMATION_CONSTANTS.RESULT_PUBLIC_BETS)
            return {
                ...state,
                // publicBets: state.publicBets.filter(
                //     (content) => content.unique_id !== action.data.unique_id,
                // ),


                publicBets: state.publicBets.map(
                    (content) => content.unique_id === action.data.unique_id ? action.data : content,
                ),
            }

        case INFORMATION_CONSTANTS.SET_PUBLIC_BETS_ALL:
            return {
                ...state,
                publicBets: action?.data
            }
        case INFORMATION_CONSTANTS.BET_ON_CHART:
            return {
                ...state,
                betOnChart: action.data
            }
        case INFORMATION_CONSTANTS.RESULT_BET:
            const ff = state.myBets.filter(
                (content) => content.unique_id !== action.data.unique_id,
            )
            return {
                ...state,
                myBets: ff,
                myBetsCount: ff?.length,
                publicBets: state.publicBets.map(i => i.unique_id === action?.data?.unique_id ? action?.data : {
                    ...i,
                    is_resulted: true,
                }),
                positionDetail: state?.positionDetail?.unique_id === action?.data?.unique_id ? {} : state.positionDetail,
                modalType: state?.positionDetail?.unique_id === action?.data?.unique_id ? '' : state.modalType,
                betOnChart: state?.betOnChart?.unique_id === action?.data?.unique_id ? {} : state.betOnChart
            }


        case INFORMATION_CONSTANTS.SET_VISIBILITY_STATUS:
            return {
                ...state,
                visibilityStatus: action.data
            }


        case INFORMATION_CONSTANTS.SET_LAST_DATA:
            return {
                ...state,
                lastData: action.data
            }

        case INFORMATION_CONSTANTS.SET_PARITY:
            console.log('INFORMATION_CONSTANTS.SET_PARITY: = ', action.data)
            localStorage.setItem('lastParity', JSON.stringify(action.data))
            return {
                ...state,
                activeParity: action.data
            }


        case INFORMATION_CONSTANTS.SET_RESOLUTION:
            return {
                ...state,
                resolution: action.data
            }
        default:
            return state;
    }
};

export default informationReducer;
